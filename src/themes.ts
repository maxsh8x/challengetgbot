export interface ThemeRoasts {
  jackpot: string[];
  veryClose: string[];
  huge: string[];
  big: string[];
  slightlyAbove: string[];
  slightlyBelow: string[];
  small: string[];
  tiny: string[];
}

export interface Theme {
  id: string;
  name: string;
  emoji: string;
  displayEmojis?: string[];  // random per-message emoji pool; falls back to emoji
  unit: string;
  minValue: number;
  maxValue: number;
  targetMin: number;
  targetMax: number;
  objectNames: string[];
  roasts: ThemeRoasts;
}

export function getDisplayEmoji(theme: Theme): string {
  const pool = theme.displayEmojis;
  if (!pool || pool.length === 0) return theme.emoji;
  return pool[Math.floor(Math.random() * pool.length)];
}

